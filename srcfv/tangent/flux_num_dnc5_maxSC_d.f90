!
!
!
!
!
!
!1st direction
      rhomrd = wd(i, j, 1)
      rhomr = w(i, j, 1)
      temp = w(i, j, 2)/rhomr
      urd = (wd(i, j, 2)-temp*rhomrd)/rhomr
      ur = temp
      temp = w(i, j, 3)/rhomr
      vrd = (wd(i, j, 3)-temp*rhomrd)/rhomr
      vr = temp
      c2rd = gam*rgaz*tlocd(i, j)
      c2r = gam*rgaz*tloc(i, j)
!
      rhomld = wd(i, j-1, 1)
      rhoml = w(i, j-1, 1)
      temp = w(i, j-1, 2)/rhoml
      uld = (wd(i, j-1, 2)-temp*rhomld)/rhoml
      ul = temp
      temp = w(i, j-1, 3)/rhoml
      vld = (wd(i, j-1, 3)-temp*rhomld)/rhoml
      vl = temp
      c2ld = gam*rgaz*tlocd(i, j-1)
      c2l = gam*rgaz*tloc(i, j-1)
!
      temp = rhomr/rhoml
      temp0 = SQRT(temp)
      IF (temp .EQ. 0.0) THEN
        rd = 0.0_8
      ELSE
        rd = (rhomrd-temp*rhomld)/(2.0*temp0*rhoml)
      END IF
      r = temp0
      temp0 = one/(one+r)
      rrd = -(temp0*rd/(one+r))
      rr = temp0
      omrrd = -rrd
      omrr = one - rr
!
      ud = rr*uld + ul*rrd + omrr*urd + ur*omrrd
      u = ul*rr + ur*omrr
      vd = rr*vld + vl*rrd + omrr*vrd + vr*omrrd
      v = vl*rr + vr*omrr
!
      c2xd = rr*c2ld + c2l*rrd + omrr*c2rd + c2r*omrrd
      c2x = c2l*rr + c2r*omrr
      nx2 = nx(i, j, 2)*nx(i, j, 2) + ny(i, j, 2)*ny(i, j, 2)
      x2d = nx(i, j, 2)*ud + ny(i, j, 2)*vd
      x2 = nx(i, j, 2)*u + ny(i, j, 2)*v
      IF (x2 .GE. 0.) THEN
        abd = x2d
        ab = x2
      ELSE
        abd = -x2d
        ab = -x2
      END IF
      temp0 = SQRT(nx2*c2x)
      IF (nx2*c2x .EQ. 0.0) THEN
        sqd = 0.0_8
      ELSE
        sqd = nx2*c2xd/(2.0*temp0)
      END IF
      sq = temp0
!
      rspecd = abd + sqd
      rspec = ab + sq
!
      rspecbisd = rspecd
      rspecbis = rspec
      IF (rspec .LT. rspecbis) THEN
        rspecd = rspecbisd
        rspec = rspecbis
      ELSE
        rspec = rspec
      END IF
      sqbisd = sqd
      sqbis = sq
      IF (sq .LT. sqbis) THEN
        sqd = sqbisd
        sq = sqbis
      ELSE
        sq = sq
      END IF
!1st direction
      DO l=j-gh,j+gh-1
!DIR$ IVDEP
        DO k=i-gh,i+gh-1
          IF (p(k-1, l) - two*p(k, l) + p(k+1, l) .GE. 0.) THEN
            abs0d = pd(k-1, l) - two*pd(k, l) + pd(k+1, l)
            abs0 = p(k-1, l) - two*p(k, l) + p(k+1, l)
          ELSE
            abs0d = two*pd(k, l) - pd(k-1, l) - pd(k+1, l)
            abs0 = -(p(k-1, l)-two*p(k, l)+p(k+1, l))
          END IF
          x3d = pd(k-1, l) + two*pd(k, l) + pd(k+1, l)
          x3 = p(k-1, l) + two*p(k, l) + p(k+1, l)
          IF (x3 .GE. 0.) THEN
            abs1d = x3d
            abs1 = x3
          ELSE
            abs1d = -x3d
            abs1 = -x3
          END IF
          k_sensor1d = (abs0d-abs0*abs1d/abs1)/abs1
          k_sensor1 = abs0/abs1
! k_sensor2 = ABS(p(k-2,l) - TWO*p(k-1,l) + p(k,l)) / &
!             ABS(p(k-2,l) + TWO*p(k-1,l) + p(k,l))
          divu2d = 2*(gradu(k, l, 1)+gradv(k, l, 2))*(gradud(k, l, 1)+&
&           gradvd(k, l, 2))
          divu2 = (gradu(k, l, 1)+gradv(k, l, 2))*(gradu(k, l, 1)+gradv(&
&           k, l, 2))
          vort2d = 2*(gradv(k, l, 1)-gradu(k, l, 2))*(gradvd(k, l, 1)-&
&           gradud(k, l, 2))
          vort2 = (gradv(k, l, 1)-gradu(k, l, 2))*(gradv(k, l, 1)-gradu(&
&           k, l, 2))
          temp0 = divu2/(divu2+vort2+1d-15)
          ducros1d = (divu2d-temp0*(divu2d+vort2d))/(divu2+vort2+1d-15)
          ducros1 = temp0
          temp0 = 10.d0*vol(k, l)
          temp = (gradu(k, l, 1)+gradv(k, l, 2))/(sq+1d-15)
          arg1d = temp0*(gradud(k, l, 1)+gradvd(k, l, 2)-temp*sqd)/(sq+&
&           1d-15)
          arg1 = temp0*temp + 2.5d0
          dxm1d = -(half*(1.0-TANH(arg1)**2)*arg1d)
          dxm1 = half*(one-TANH(arg1))
! divu2     = (gradu(k-1,l,1)+gradv(k-1,l,2)) * (gradu(k-1,l,1)+gradv(k-1,l,2))
! vort2     = (gradv(k-1,l,1)-gradu(k-1,l,2)) * (gradv(k-1,l,1)-gradu(k-1,l,2))
! ducros2   = divu2/(divu2+vort2+1d-15)
! dxm2       = HALF*(ONE-tanh(2.5d0+10.d0*vol(k,l)/(sq+1d-15)*(gradu(k-1,l,1)+gradv(k-1,l,2))) )
! coef      = MAX( k_sensor1*ducros1, &
!                  k_sensor2*ducros2)
! coef      = MAX( k_sensor1*ducros1*dxm1, &
!                  k_sensor2*ducros2*dxm2)
          coefd = dxm1*(ducros1*k_sensor1d+k_sensor1*ducros1d) + &
&           k_sensor1*ducros1*dxm1d
          coef = k_sensor1*ducros1*dxm1
          eps2arrad(k, l) = k2*coefd
          eps2arra(k, l) = k2*coef
        END DO
      END DO
      IF (eps2arra(i, j-2) .LT. eps2arra(i, j-3)) THEN
        y13d = eps2arrad(i, j-3)
        y13 = eps2arra(i, j-3)
      ELSE
        y13d = eps2arrad(i, j-2)
        y13 = eps2arra(i, j-2)
      END IF
      IF (eps2arra(i, j+2) .LT. y13) THEN
        y12d = y13d
        y12 = y13
      ELSE
        y12d = eps2arrad(i, j+2)
        y12 = eps2arra(i, j+2)
      END IF
      IF (eps2arra(i-3, j) .LT. y12) THEN
        y11d = y12d
        y11 = y12
      ELSE
        y11d = eps2arrad(i-3, j)
        y11 = eps2arra(i-3, j)
      END IF
      IF (eps2arra(i-2, j) .LT. y11) THEN
        y10d = y11d
        y10 = y11
      ELSE
        y10d = eps2arrad(i-2, j)
        y10 = eps2arra(i-2, j)
      END IF
      IF (eps2arra(i+2, j) .LT. y10) THEN
        y9d = y10d
        y9 = y10
      ELSE
        y9d = eps2arrad(i+2, j)
        y9 = eps2arra(i+2, j)
      END IF
      IF (eps2arra(i-1, j-1) .LT. y9) THEN
        y8d = y9d
        y8 = y9
      ELSE
        y8d = eps2arrad(i-1, j-1)
        y8 = eps2arra(i-1, j-1)
      END IF
      IF (eps2arra(i-1, j+1) .LT. y8) THEN
        y7d = y8d
        y7 = y8
      ELSE
        y7d = eps2arrad(i-1, j+1)
        y7 = eps2arra(i-1, j+1)
      END IF
      IF (eps2arra(i+1, j-1) .LT. y7) THEN
        y6d = y7d
        y6 = y7
      ELSE
        y6d = eps2arrad(i+1, j-1)
        y6 = eps2arra(i+1, j-1)
      END IF
      IF (eps2arra(i+1, j+1) .LT. y6) THEN
        y5d = y6d
        y5 = y6
      ELSE
        y5d = eps2arrad(i+1, j+1)
        y5 = eps2arra(i+1, j+1)
      END IF
      IF (eps2arra(i, j+1) .LT. y5) THEN
        y4d = y5d
        y4 = y5
      ELSE
        y4d = eps2arrad(i, j+1)
        y4 = eps2arra(i, j+1)
      END IF
      IF (eps2arra(i+1, j) .LT. y4) THEN
        y3d = y4d
        y3 = y4
      ELSE
        y3d = eps2arrad(i+1, j)
        y3 = eps2arra(i+1, j)
      END IF
      IF (eps2arra(i, j-1) .LT. y3) THEN
        y2d = y3d
        y2 = y3
      ELSE
        y2d = eps2arrad(i, j-1)
        y2 = eps2arra(i, j-1)
      END IF
      IF (eps2arra(i-1, j) .LT. y2) THEN
        y1d = y2d
        y1 = y2
      ELSE
        y1d = eps2arrad(i-1, j)
        y1 = eps2arra(i-1, j)
      END IF
      IF (eps2arra(i, j) .LT. y1) THEN
        eps2d = y1d
        eps2 = y1
      ELSE
        eps2d = eps2arrad(i, j)
        eps2 = eps2arra(i, j)
      END IF
      IF (zero .LT. k4 - 12.d0*eps2) THEN
        eps4d = -(12.d0*eps2d)
        eps4 = k4 - 12.d0*eps2
      ELSE
        eps4 = zero
        eps4d = 0.0_8
      END IF
! eps4      = MAX(ZERO,k4 - 3.d0/70.d0 * eps2)
      diffrod = wd(i, j, 1) - wd(i-1, j, 1)
      diffro = w(i, j, 1) - w(i-1, j, 1)
      diffroud = wd(i, j, 2) - wd(i-1, j, 2)
      diffrou = w(i, j, 2) - w(i-1, j, 2)
      diffrovd = wd(i, j, 3) - wd(i-1, j, 3)
      diffrov = w(i, j, 3) - w(i-1, j, 3)
      diffrowd = wd(i, j, 4) - wd(i-1, j, 4)
      diffrow = w(i, j, 4) - w(i-1, j, 4)
      diffroed = wd(i, j, 5) - wd(i-1, j, 5)
      diffroe = w(i, j, 5) - w(i-1, j, 5)
!
      temp0 = eps2*diffro + eps4*predro1
      dissro1d = temp0*rspecd + rspec*(diffro*eps2d+eps2*diffrod+predro1&
&       *eps4d+eps4*predro1d)
      temp0 = eps2*diffrou + eps4*predrou1
      dissrou1d = temp0*rspecd + rspec*(diffrou*eps2d+eps2*diffroud+&
&       predrou1*eps4d+eps4*predrou1d)
      temp0 = eps2*diffrov + eps4*predrov1
      dissrov1d = temp0*rspecd + rspec*(diffrov*eps2d+eps2*diffrovd+&
&       predrov1*eps4d+eps4*predrov1d)
      temp0 = eps2*diffrow + eps4*predrow1
      dissrow1d = temp0*rspecd + rspec*(diffrow*eps2d+eps2*diffrowd+&
&       predrow1*eps4d+eps4*predrow1d)
      temp0 = eps2*diffroe + eps4*predroe1
      dissroe1d = temp0*rspecd + rspec*(diffroe*eps2d+eps2*diffroed+&
&       predroe1*eps4d+eps4*predroe1d)
      diffrod = wd(i, j, 1) - wd(i, j-1, 1)
      diffro = w(i, j, 1) - w(i, j-1, 1)
      diffroud = wd(i, j, 2) - wd(i, j-1, 2)
      diffrou = w(i, j, 2) - w(i, j-1, 2)
      diffrovd = wd(i, j, 3) - wd(i, j-1, 3)
      diffrov = w(i, j, 3) - w(i, j-1, 3)
      diffrowd = wd(i, j, 4) - wd(i, j-1, 4)
      diffrow = w(i, j, 4) - w(i, j-1, 4)
      diffroed = wd(i, j, 5) - wd(i, j-1, 5)
      diffroe = w(i, j, 5) - w(i, j-1, 5)
      temp0 = eps2*diffro + eps4*predro2
      dissro2d = temp0*rspecd + rspec*(diffro*eps2d+eps2*diffrod+predro2&
&       *eps4d+eps4*predro2d)
      temp0 = eps2*diffrou + eps4*predrou2
      dissrou2d = temp0*rspecd + rspec*(diffrou*eps2d+eps2*diffroud+&
&       predrou2*eps4d+eps4*predrou2d)
      temp0 = eps2*diffrov + eps4*predrov2
      dissrov2d = temp0*rspecd + rspec*(diffrov*eps2d+eps2*diffrovd+&
&       predrov2*eps4d+eps4*predrov2d)
      temp0 = eps2*diffrow + eps4*predrow2
      dissrow2d = temp0*rspecd + rspec*(diffrow*eps2d+eps2*diffrowd+&
&       predrow2*eps4d+eps4*predrow2d)
      temp0 = eps2*diffroe + eps4*predroe2
      dissroe2d = temp0*rspecd + rspec*(diffroe*eps2d+eps2*diffroed+&
&       predroe2*eps4d+eps4*predroe2d)
      sc1 = nx(i, j, 1)
      sc2 = ny(i, j, 1)
      arg1 = sc1*sc1 + sc2*sc2
      sn = SQRT(arg1)
      invsn = one/sn
      nxloc = sc1*invsn
      nyloc = sc2*invsn
      hnd(i, j, 1, 1) = fxro1d - dissro1d
!
      hnd(i, j, 2, 1) = fxrou1d - dissrou1d - sn*(nxloc*fvrou1d+nyloc*&
&       gvrou1d)
!
      hnd(i, j, 3, 1) = fxrov1d - dissrov1d - sn*(nxloc*fvrov1d+nyloc*&
&       gvrov1d)
!
      hnd(i, j, 4, 1) = fxrow1d - dissrow1d - sn*(nxloc*fvrow1d+nyloc*&
&       gvrow1d)
!
      hnd(i, j, 5, 1) = fxroe1d - dissroe1d - sn*(nxloc*fvroe1d+nyloc*&
&       gvroe1d)
      sc1 = nx(i, j, 2)
      sc2 = ny(i, j, 2)
      arg1 = sc1*sc1 + sc2*sc2
      sn = SQRT(arg1)
      invsn = one/sn
      nxloc = sc1*invsn
      nyloc = sc2*invsn
      hnd(i, j, 1, 2) = fxro2d - dissro2d
!
      hnd(i, j, 2, 2) = fxrou2d - dissrou2d - sn*(nxloc*fvrou2d+nyloc*&
&       gvrou2d)
!
      hnd(i, j, 3, 2) = fxrov2d - dissrov2d - sn*(nxloc*fvrov2d+nyloc*&
&       gvrov2d)
!
      hnd(i, j, 4, 2) = fxrow2d - dissrow2d - sn*(nxloc*fvrow2d+nyloc*&
&       gvrow2d)
!
      hnd(i, j, 5, 2) = fxroe2d - dissroe2d - sn*(nxloc*fvroe2d+nyloc*&
&       gvroe2d)
    END DO
  END DO
  residud = 0.0_8
!
! fluxes balance
  DO j=1,jm
!DIR$ IVDEP
    DO i=1,im
      residud(i, j, 1) = hnd(i, j, 1, 1) - hnd(i+1, j, 1, 1) + hnd(i, j&
&       , 1, 2) - hnd(i, j+1, 1, 2)
      residud(i, j, 2) = hnd(i, j, 2, 1) - hnd(i+1, j, 2, 1) + hnd(i, j&
&       , 2, 2) - hnd(i, j+1, 2, 2)
      residud(i, j, 3) = hnd(i, j, 3, 1) - hnd(i+1, j, 3, 1) + hnd(i, j&
&       , 3, 2) - hnd(i, j+1, 3, 2)
      residud(i, j, 4) = hnd(i, j, 4, 1) - hnd(i+1, j, 4, 1) + hnd(i, j&
&       , 4, 2) - hnd(i, j+1, 4, 2)
      residud(i, j, 5) = hnd(i, j, 5, 1) - hnd(i+1, j, 5, 1) + hnd(i, j&
&       , 5, 2) - hnd(i, j+1, 5, 2)
    END DO
  END DO
END SUBROUTINE FLUX_NUM_DNC5_MAXSC_2D_D
