! dxm2       = HALF*(ONE-tanh(2.5d0+10.d0*vol(i,j-1)/(sq+1.d-15)*divu) )
      arg1d = nx2*c2ld
      arg1 = c2l*nx2
      temp0 = SQRT(arg1)
      IF (arg1 .EQ. 0.0) THEN
        result1d = 0.0_8
      ELSE
        result1d = arg1d/(2.0*temp0)
      END IF
      result1 = temp0
      temp0 = 10.d0*vol(i, j-1)
      temp = divu/(result1+1.d-15)
      arg2d = temp0*(divud-temp*result1d)/(result1+1.d-15)
      arg2 = temp0*temp + 2.5d0
      dxm2d = -(half*(1.0-TANH(arg2)**2)*arg2d)
      dxm2 = half*(one-TANH(arg2))
      IF (k_sensor1 .LT. k_sensor2) THEN
        max2d = k_sensor2d
        max2 = k_sensor2
      ELSE
        max2d = k_sensor1d
        max2 = k_sensor1
      END IF
      IF (ducros1 .LT. ducros2) THEN
        max4d = ducros2d
        max4 = ducros2
      ELSE
        max4d = ducros1d
        max4 = ducros1
      END IF
      IF (dxm1 .LT. dxm2) THEN
        max6d = dxm2d
        max6 = dxm2
      ELSE
        max6d = dxm1d
        max6 = dxm1
      END IF
!coef      = MAX( k_sensor1*ducros1*dxm1, &
!                 k_sensor2*ducros2*dxm2)
      coefd = max6*(max4*max2d+max2*max4d) + max2*max4*max6d
      coef = max2*max4*max6
! extension of shock capturing
!k_sensor1 = ABS(p(i,j) - TWO*p(i,j+1) + p(i,j+2)) / &
!            ABS(p(i,j) + TWO*p(i,j+1) + p(i,j+2))
!
!k_sensor2 = ABS(p(i,j-3) - TWO*p(i,j-2) + p(i,j-1)) / &
!            ABS(p(i,j-3) + TWO*p(i,j-2) + p(i,j-1))
!
!divu      = gradu(i,j+1,1)+gradv(i,j+1,2)
!divu2     = divu*divu
!vort2     = (gradv(i,j+1,1)-gradu(i,j+1,2)) * (gradv(i,j+1,1)-gradu(i,j+1,2))
!ducros1   = divu2/(divu2+vort2+1d-15)
!dxm1      = HALF*(ONE-tanh(2.5d0+10.d0*vol(i,j+1)/(sq+1.d-15)*divu) )
!
!
!divu      = gradu(i,j-2,1)+gradv(i,j-2,2)
!divu2     = divu*divu
!vort2     = (gradv(i,j-2,1)-gradu(i,j-2,2)) * (gradv(i,j-2,1)-gradu(i,j-2,2))
!ducros2   = divu2/(divu2+vort2+1d-15)
!dxm2      = HALF*(ONE-tanh(2.5d0+10.d0*vol(i,j-2)/(sq+1.d-15)*divu) )
!
!coef      = MAX( k_sensor1*ducros1*dxm1, &
!                 k_sensor2*ducros2*dxm2, coef)
!coef      = MAX(sensor(i  ,j-1,2), sensor(i  ,j,2), &
!                sensor(i-1,j-1,2), sensor(i-1,j,2), &
!                sensor(i+1,j-1,2), sensor(i+1,j,2)  )
!
!coef      = MAX(sensor(i,j-3,2), sensor(i,j-2,2), &
!                sensor(i,j-1,2), sensor(i,j  ,2), &
!                sensor(i,j+1,2), sensor(i,j+2,2)  )
!
!coef      = MAX(sensor(i,j-2,2), &
!                sensor(i,j-1,2), sensor(i,j,2), &
!                sensor(i,j+1,2))
!
!rspec      = rconv(i,j,2)
      eps2d = k2*coefd
      eps2 = k2*coef
      IF (zero .LT. k4 - 12.d0*eps2) THEN
        eps4d = -(12.d0*eps2d)
        eps4 = k4 - 12.d0*eps2
      ELSE
        eps4 = zero
        eps4d = 0.0_8
      END IF
      diffrod = half*(wd(i, j, 1)-wd(i, j-1, 1))
      diffro = half*(w(i, j, 1)-w(i, j-1, 1))
      diffroud = half*(wd(i, j, 2)-wd(i, j-1, 2))
      diffrou = half*(w(i, j, 2)-w(i, j-1, 2))
      diffrovd = half*(wd(i, j, 3)-wd(i, j-1, 3))
      diffrov = half*(w(i, j, 3)-w(i, j-1, 3))
      diffrowd = half*(wd(i, j, 4)-wd(i, j-1, 4))
      diffrow = half*(w(i, j, 4)-w(i, j-1, 4))
      diffroed = half*(wd(i, j, 5)-wd(i, j-1, 5))
      diffroe = half*(w(i, j, 5)-w(i, j-1, 5))
!
!
!     if (eps4.gt.1.d-12) then
! #include "rhs/wiggle_dirj.F"
!     endif
!
      temp0 = eps2*diffro + eps4*predro2
      dissro2d = temp0*rspecd + rspec*(diffro*eps2d+eps2*diffrod+predro2&
&       *eps4d+eps4*predro2d)
      temp0 = eps2*diffrou + eps4*predrou2
      dissrou2d = temp0*rspecd + rspec*(diffrou*eps2d+eps2*diffroud+&
&       predrou2*eps4d+eps4*predrou2d)
      temp0 = eps2*diffrov + eps4*predrov2
      dissrov2d = temp0*rspecd + rspec*(diffrov*eps2d+eps2*diffrovd+&
&       predrov2*eps4d+eps4*predrov2d)
      temp0 = eps2*diffrow + eps4*predrow2
      dissrow2d = temp0*rspecd + rspec*(diffrow*eps2d+eps2*diffrowd+&
&       predrow2*eps4d+eps4*predrow2d)
      temp0 = eps2*diffroe + eps4*predroe2
      dissroe2d = temp0*rspecd + rspec*(diffroe*eps2d+eps2*diffroed+&
&       predroe2*eps4d+eps4*predroe2d)
      sc1 = nx(i, j, 1)
      sc2 = ny(i, j, 1)
      arg1 = sc1*sc1 + sc2*sc2
      sn = SQRT(arg1)
      invsn = one/sn
      nxloc = sc1*invsn
      nyloc = sc2*invsn
      hnd(i, j, 1, 1) = fxro1d - dissro1d
!
      hnd(i, j, 2, 1) = fxrou1d - dissrou1d - sn*(nxloc*fvrou1d+nyloc*&
&       gvrou1d)
!
      hnd(i, j, 3, 1) = fxrov1d - dissrov1d - sn*(nxloc*fvrov1d+nyloc*&
&       gvrov1d)
!
      hnd(i, j, 4, 1) = fxrow1d - dissrow1d - sn*(nxloc*fvrow1d+nyloc*&
&       gvrow1d)
!
      hnd(i, j, 5, 1) = fxroe1d - dissroe1d - sn*(nxloc*fvroe1d+nyloc*&
&       gvroe1d)
      sc1 = nx(i, j, 2)
      sc2 = ny(i, j, 2)
      arg1 = sc1*sc1 + sc2*sc2
      sn = SQRT(arg1)
      invsn = one/sn
      nxloc = sc1*invsn
      nyloc = sc2*invsn
      hnd(i, j, 1, 2) = fxro2d - dissro2d
!
      hnd(i, j, 2, 2) = fxrou2d - dissrou2d - sn*(nxloc*fvrou2d+nyloc*&
&       gvrou2d)
!
      hnd(i, j, 3, 2) = fxrov2d - dissrov2d - sn*(nxloc*fvrov2d+nyloc*&
&       gvrov2d)
!
      hnd(i, j, 4, 2) = fxrow2d - dissrow2d - sn*(nxloc*fvrow2d+nyloc*&
&       gvrow2d)
!
      hnd(i, j, 5, 2) = fxroe2d - dissroe2d - sn*(nxloc*fvroe2d+nyloc*&
&       gvroe2d)
    END DO
  END DO
  residud = 0.0_8
!
! fluxes balance
  DO j=1,jm
!DIR$ IVDEP
    DO i=1,im
!dist   = abs(yc - yaxe)
! x = axe
! y = radial
! z = theta
      distm1 = one/(yc(i, j)-ym)
!if (distm1.gt.1.d-15) then
!   distm1    = ONE/abs( distm1 ) ! + 1.d-15
!endif
      voldistm1 = vol(i, j)*distm1
      lambdad = cpprandtl*mud(i, j)
      lambda = mu(i, j)*cpprandtl
      temp0 = w(i, j, 3)/w(i, j, 1)
      urd = (wd(i, j, 3)-temp0*wd(i, j, 1))/w(i, j, 1)
      ur = temp0
      temp0 = w(i, j, 4)/w(i, j, 1)
      uthetad = (wd(i, j, 4)-temp0*wd(i, j, 1))/w(i, j, 1)
      utheta = temp0
! htot = htot*ro
      htotd = wd(i, j, 5) + pd(i, j)
      htot = w(i, j, 5) + p(i, j)
!src1_r / (vol/r)   =   ro * ur
!src2_r / (vol/r)   =   ro * ux * ur
!src3_r / (vol/r)   =   ro * ur**2
!src4_r / (vol/r)   =   ro * ur * utheta
!src5_r / (vol/r)   =   ro * ur * htot
!src1_t / (vol/r)   =   0
!src2_t / (vol/r)   =   0
!src3_t / (vol/r)   = - ro * utheta**2
!src4_t / (vol/r)   =   ro * ur * utheta
!src5_t / (vol/r)   =   0
! ro*ur
      srce1d = wd(i, j, 3)
      srce2d = w(i, j, 2)*urd + ur*wd(i, j, 2)
      srce3d = w(i, j, 3)*urd + ur*wd(i, j, 3) - utheta*wd(i, j, 4) - w(&
&       i, j, 4)*uthetad
      srce4d = two*(w(i, j, 4)*urd+ur*wd(i, j, 4))
      srce5d = htot*urd + ur*htotd
      divud = gradud(i, j, 1) + gradvd(i, j, 2) + distm1*velyd(i, j)
      divu = gradu(i, j, 1) + gradv(i, j, 2) + vely(i, j)*distm1
      temp0 = gradv(i, j, 1) + gradu(i, j, 2)
      t_xyd = temp0*mud(i, j) + mu(i, j)*(gradvd(i, j, 1)+gradud(i, j, 2&
&       ))
      t_xy = mu(i, j)*temp0
      t_yyd = two*(gradv(i, j, 2)*mud(i, j)+mu(i, j)*gradvd(i, j, 2)) - &
&       twothird*(divu*mud(i, j)+mu(i, j)*divud)
      t_yy = two*mu(i, j)*gradv(i, j, 2) - twothird*mu(i, j)*divu
! +gradv(i,j,3)
      temp0 = gradw(i, j, 2) - distm1*utheta
      t_yzd = temp0*mud(i, j) + mu(i, j)*(gradwd(i, j, 2)-distm1*uthetad&
&       )
      t_yz = mu(i, j)*temp0
      srcv2d = t_xyd
!- t_zz
      srcv3d = t_yyd
      srcv4d = two*t_yzd
      srcv5d = t_xy*velxd(i, j) + velx(i, j)*t_xyd + t_yy*velyd(i, j) + &
&       vely(i, j)*t_yyd + t_yz*velzd(i, j) + velz(i, j)*t_yzd + &
&       grad_temp(i, j, 2)*lambdad + lambda*grad_tempd(i, j, 2)
      src1d = -(voldistm1*srce1d)
      src2d = -(voldistm1*(srce2d-srcv2d))
      src3d = -(voldistm1*(srce3d-srcv3d))
      src4d = -(voldistm1*(srce4d-srcv4d))
      src5d = -(voldistm1*(srce5d-srcv5d))
      residud(i, j, 1) = src1d - hnd(i+1, j, 1, 1) + hnd(i, j, 1, 1) + &
&       hnd(i, j, 1, 2) - hnd(i, j+1, 1, 2)
      residud(i, j, 2) = src2d - hnd(i+1, j, 2, 1) + hnd(i, j, 2, 1) + &
&       hnd(i, j, 2, 2) - hnd(i, j+1, 2, 2)
      residud(i, j, 3) = src3d - hnd(i+1, j, 3, 1) + hnd(i, j, 3, 1) + &
&       hnd(i, j, 3, 2) - hnd(i, j+1, 3, 2)
      residud(i, j, 4) = src4d - hnd(i+1, j, 4, 1) + hnd(i, j, 4, 1) + &
&       hnd(i, j, 4, 2) - hnd(i, j+1, 4, 2)
      residud(i, j, 5) = src5d - hnd(i+1, j, 5, 1) + hnd(i, j, 5, 1) + &
&       hnd(i, j, 5, 2) - hnd(i, j+1, 5, 2)
    END DO
  END DO
END SUBROUTINE FLUX_NUM_DNC5_NOWALL_POLAR_2D_D
