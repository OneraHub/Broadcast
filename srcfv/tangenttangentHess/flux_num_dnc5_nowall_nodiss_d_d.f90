!
      hndd(i, j, 5, 1) = fxroe1dd - sn*(nxloc*fvroe1dd+nyloc*gvroe1dd)
      sc1 = nx(i, j, 2)
      sc2 = ny(i, j, 2)
      arg1 = sc1*sc1 + sc2*sc2
      sn = SQRT(arg1)
      invsn = one/sn
      nxloc = sc1*invsn
      nyloc = sc2*invsn
      hndd(i, j, 1, 2) = fxro2dd
!
      hndd(i, j, 2, 2) = fxrou2dd - sn*(nxloc*fvrou2dd+nyloc*gvrou2dd)
!
      hndd(i, j, 3, 2) = fxrov2dd - sn*(nxloc*fvrov2dd+nyloc*gvrov2dd)
