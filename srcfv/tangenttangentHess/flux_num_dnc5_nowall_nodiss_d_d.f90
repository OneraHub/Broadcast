&       9.d0*tlocdd(i, j-1)-tlocdd(i, j-2)+9.d0*tlocdd(i, j)-tlocdd(i, j&
&       +1))+tlocdd(i+1, j-2)-9.d0*tlocdd(i+1, j-1)+tlocdd(i+1, j+1)-&
