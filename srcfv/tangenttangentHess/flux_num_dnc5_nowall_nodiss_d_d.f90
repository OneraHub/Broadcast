!
      hndd(i, j, 5, 2) = fxroe2dd - sn*(nxloc*fvroe2dd+nyloc*gvroe2dd)
    END DO
  END DO
  residudd = 0.0_8
!
! fluxes balance
  DO j=1,jm
!GCC$ IVDEP
    DO i=1,im
      residudd(i, j, 1) = hndd(i, j, 1, 1) - hndd(i+1, j, 1, 1) + hndd(i&
&       , j, 1, 2) - hndd(i, j+1, 1, 2)
