      residudd(i, j, 4) = hndd(i, j, 4, 1) - hndd(i+1, j, 4, 1) + hndd(i&
&       , j, 4, 2) - hndd(i, j+1, 4, 2)
      residudd(i, j, 5) = hndd(i, j, 5, 1) - hndd(i+1, j, 5, 1) + hndd(i&
&       , j, 5, 2) - hndd(i, j+1, 5, 2)
    END DO
  END DO
END SUBROUTINE FLUX_NUM_DNC5_NOWALL_NODISS_2D_D_D
