      gvrou2d = (uy+vx)*mmud + mmu*(uyd+vxd)
      gvrou2d0 = (uy+vx)*mmud0 + mmu*(uyd0+vxd0)
      gvrou2 = mmu*(uy+vx)
      gvrov2dd = twothird*(mmud*(two*vyd0-uxd0)+(two*vy-ux)*mmudd+(two*&
